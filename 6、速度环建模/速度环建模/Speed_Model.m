%% 利用闭环传递函数反求解机械传递函数
K = 1.5 * p * Psi_O;
Kf = pi / 30 * p * Psi_O;
G_RL = 1/(Rs_O+Lq_O*s);

hat_H_p2 = d2c(sys_tf_p2,'zoh');
hat_G_p2 = hat_H_p2/G_RL/(K - K * Kf * hat_H_p2);
hat_C_p2 = feedback(hat_G_p2*G_RL*K,Kf);
DC(1) = dcgain(hat_G_p2);
Pole(1,:) = pole(hat_G_p2);
Zero(1,:) = zero(hat_G_p2);

hat_H_p4 = d2c(sys_tf_p4,'zoh');
hat_G_p4 = hat_H_p4/G_RL/(K - K * Kf * hat_H_p4);
