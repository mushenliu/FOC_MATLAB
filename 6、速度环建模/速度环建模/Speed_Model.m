%% 测量
data = readmatrix("Speed_2.txt", 'NumHeaderLines', 1);
fprintf('工作点2V：\n');
Uq = data(end-2046:end,9) - 2;
Id = data(end-2046:end,5);
Iq = data(end-2046:end,6);
n = data(end-2046:end,3);
DCGAIN(1) = mean(n)/2;
n = n - mean(n);
we = n / 60 * 2 * pi * p;
% Uq = Uq - we.*Psi_O;
Teqd = 1.5*p.*Iq.*((Ld_O-Lq_O).*Id+Psi_O);
Teq = 1.5*p*Psi_O.*Iq;
Eqd = we.*(Psi_O+Ld_O.*Id);
Eq = we.*Psi_O;
[sys_tf_p2,~] = DataProcess(Uq,n,k,m,Ts);
sys_tf_p2 = sys_tf_p2 * DCGAIN(1) / dcgain(sys_tf_p2);
figure(Name='理想假设分析');
subplot(1,2,1);
plot(Teq);
hold on;
plot(Teqd);
legend('id=0电磁转矩','id≠0电磁转矩');
subplot(1,2,2);
plot(Eq);
hold on;
plot(Eqd);
legend('id=0反电动势','id≠0反电动势');
fprintf('电磁转矩理想化分析：\n');
Signal_Analyse(Teq,Teqd);
fprintf('反电动势分析：\n');
Signal_Analyse(Eq,Eqd);
figure(Name='相干函数对比');
[gamma_Uq, F] = mscohere(Uq,n, hamming(256), 16, 2048, 1/Ts);
gamma_Uq = 20*log10(gamma_Uq);
[gamma_Iq, ~] = mscohere(Iq,n, hamming(256), 16, 2048, 1/Ts);
gamma_Iq = 20*log10(gamma_Iq);
semilogx(F,gamma_Uq,F,gamma_Iq);
grid on;
grid minor;
xlabel('频率/Hz');
ylabel('分贝/dB');
subtitle('输入输出相干函数');
legend('Uq->n','Iq->n');
ylim([-0.5,0]);
xlim([1,300]);

